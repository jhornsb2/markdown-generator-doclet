package com.jhornsb2.doclet.generator.markdown.elements;

/**
 * Interface representing an element that has a return type (e.g., method or
 * constructor).
 */
public interface IReturnType {
	/**
	 * Returns the fully qualified name of the return type of the element.
	 * @return the fully qualified name of the return type
	 */
	String getReturnType();
}
